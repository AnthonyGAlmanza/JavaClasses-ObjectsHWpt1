package objectsJavaHW;

public class Pet {

	private String name;
	private Integer age;
	private String location;
	private String type;
	
	public String getName() {
		return name;
	}
	
	public Integer getAge() {
		return age;
	}
	
	public String getType() {
		return type;
	}
	
	public void setName() {
		this.name = name;
	}
	
	public void setAge() {
		this.age = age;
	}
	
	public void setLocation() {
		this.location = location;
	}
	

}
